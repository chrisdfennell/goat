                <h2 class="text-2xl font-bold text-slate-900 mb-4">Common Goat Terminology</h2>
                <div class="grid md:grid-cols-2 gap-x-8 gap-y-6">
                    <div><p><strong class="text-slate-800">Buck:</strong> An intact (uncastrated) male goat.</p></div>
                    <div><p><strong class="text-slate-800">Doe:</strong> A female goat.</p></div>
                    <div><p><strong class="text-slate-800">Wether:</strong> A castrated male goat.</p></div>
                    <div><p><strong class="text-slate-800">Kid:</strong> A baby goat, male or female.</p></div>
                    <div><p><strong class="text-slate-800">Kidding:</strong> The act of a doe giving birth.</p></div>
                    <div><p><strong class="text-slate-800">Dam:</strong> The mother of a goat.</p></div>
                    <div><p><strong class="text-slate-800">Sire:</strong> The father of a goat.</p></div>
                    <div><p><strong class="text-slate-800">Freshen:</strong> To begin producing milk after giving birth.</p></div>
                    <div><p><strong class="text-slate-800">Dry Doe:</strong> A doe that is not currently lactating (producing milk).</p></div>
                    <div><p><strong class="text-slate-800">Polled:</strong> A goat that is naturally hornless due to genetics.</p></div>
                    <div><p><strong class="text-slate-800">Disbudding:</strong> The process of removing horn buds on a young kid to prevent horn growth.</p></div>
                    <div><p><strong class="text-slate-800">Scurs:</strong> Small, misshapen horn growths that can occur after an incomplete disbudding.</p></div>
                    <div><p><strong class="text-slate-800">Rumen:</strong> The largest of the four stomach compartments in a goat, responsible for fermentation.</p></div>
                    <div><p><strong class="text-slate-800">Browse:</strong> Woody plants, shrubs, and leaves that goats prefer to eat.</p></div>
                </div>
            </section>
            <section class="bg-white p-6 rounded-lg shadow-md border border-slate-200">
                <h2 class="text-2xl font-bold text-slate-900 mb-4">Health &amp; Acronyms</h2>
                <div class="grid md:grid-cols-2 gap-x-8 gap-y-6">
                    <div><p><strong class="text-slate-800">BCS (Body Condition Score):</strong> A 1-5 scale used to assess a goat's body fat reserves.</p></div>
                    <div><p><strong class="text-slate-800">CDT:</strong> The core vaccine for goats, protecting against Clostridium perfringens types C &amp; D (enterotoxemia) and Tetanus.</p></div>
                    <div><p><strong class="text-slate-800">FAMACHA:</strong> A system for assessing anemia (and thus, barber pole worm load) by checking the color of a goat's lower eyelids.</p></div>
                    <div><p><strong class="text-slate-800">Coccidiosis:</strong> A common and potentially deadly parasitic disease in kids, causing severe diarrhea (scours).</p></div>
                    <div><p><strong class="text-slate-800">CAE (Caprine Arthritis Encephalitis):</strong> A viral disease, primarily in dairy goats, that can cause arthritis, mastitis, and encephalitis.</p></div>
                    <div><p><strong class="text-slate-800">CL (Caseous Lymphadenitis):</strong> A chronic, contagious bacterial infection that causes abscesses in the lymph nodes.</p></div>
                    <div><p><strong class="text-slate-800">LGD:</strong> Livestock Guardian Dog, such as a Great Pyrenees or Anatolian Shepherd.</p></div>
                    <div><p><strong class="text-slate-800">Ruminant:</strong> An animal with a four-chamber stomach that chews cud, like goats, sheep, and cattle.</p></div>
                </div>
